    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        ClienteController controller = new ClienteController();
        Cliente cliente = new Cliente(0,inputNome.getText(), inputEndereco.getText(),inputCep.getText(),inputTelefone.getText(), inputEmail.getText());
        int result = controller.cadastroCliente(cliente);
        if (result == 0){
            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_btnCadastroActionPerformed

